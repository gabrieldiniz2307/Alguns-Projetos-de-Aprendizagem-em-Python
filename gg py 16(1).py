import statistics as st

x = [1, 2, 3, 4, 5]
y = [2, 3, 4, 5, 6]
media_x = st.mean(x)
media_y = st.mean(y)

varianca=sum((x[i]-media_x)**2 for i in range(len(x)))
covarianca=sum((x[i]-media_x)*(y[i]-media_y) for i in range(len(x)))

#Coeficiente de Inclina√ßao
b1 = covarianca / varianca
#Coeficiente de Intercepto
b0 = media_y-b1*media_x

print(b0)